let selectedOrigin = localStorage.getItem("hellenesOrigin") || "athens";
let currentLanguage = localStorage.getItem("hellenesLanguage") || "en";

function t(key) {
  return I18N[currentLanguage]?.[key] || I18N.en[key] || key;
}

function originText(originKey) {
  const origin = ORIGINS[originKey] || ORIGINS.athens;
  return origin[currentLanguage] || origin.en;
}

function applyLanguage(lang = currentLanguage) {
  currentLanguage = lang === "el" ? "el" : "en";
  localStorage.setItem("hellenesLanguage", currentLanguage);

  document.documentElement.lang = currentLanguage;
  document.documentElement.setAttribute("data-language", currentLanguage);

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  document.querySelectorAll("[data-origin-label]").forEach(el => {
    el.textContent = originText(el.dataset.originLabel).name;
  });

  document.querySelectorAll("[data-origin-mood]").forEach(el => {
    el.textContent = originText(el.dataset.originMood).mood;
  });

  const toggle = document.getElementById("languageToggle");
  if (toggle) {
    toggle.textContent = currentLanguage === "en" ? "EL" : "EN";
    toggle.setAttribute("aria-label", currentLanguage === "en" ? "Switch to Greek" : "Αλλαγή στα Αγγλικά");
  }

  document.title = document.body.classList.contains("home-body")
    ? t("meta.homeTitle")
    : document.body.classList.contains("auth-body")
      ? t("meta.authTitle")
      : "Hellenes";

  renderAttributes();
  setOrigin(selectedOrigin, { skipLanguageApply: true });
  updateHomeOriginDetail();
}

function toggleLanguage() {
  applyLanguage(currentLanguage === "en" ? "el" : "en");
}


function updateHomeOriginCardsSelection() {
  document.querySelectorAll("[data-home-origin]").forEach(card => {
    card.classList.toggle("is-selected", card.dataset.homeOrigin === selectedOrigin);
  });
}

function updateHomeOriginDetail() {
  const descriptionRoot = document.getElementById("homeOriginDescription");
  const bonusesRoot = document.getElementById("homeOriginBonuses");
  if (!descriptionRoot && !bonusesRoot) return;

  const text = originText(selectedOrigin);

  if (descriptionRoot) {
    descriptionRoot.textContent = text.description;
  }

  if (bonusesRoot) {
    bonusesRoot.innerHTML = text.bonuses.map(bonus => `<div>${bonus}</div>`).join("");
  }

  updateHomeOriginCardsSelection();
}

function wireHomeOriginCards() {
  document.querySelectorAll("[data-home-origin]").forEach(card => {
    const choose = () => {
      setOrigin(card.dataset.homeOrigin);
      document.getElementById("homeOriginDetail")?.scrollIntoView({
        behavior: "smooth",
        block: "nearest"
      });
    };

    card.addEventListener("click", choose);
    card.addEventListener("keydown", event => {
      if (event.key === "Enter" || event.key === " ") {
        event.preventDefault();
        choose();
      }
    });
  });
}


function updateRegistrationCardsSelection() {
  document.querySelectorAll("[data-registration-origin]").forEach(card => {
    card.classList.toggle("is-selected", card.dataset.registrationOrigin === selectedOrigin);
  });
}

function setOrigin(originKey, options = {}) {
  const safeOrigin = ORIGINS[originKey] ? originKey : "athens";
  const origin = ORIGINS[safeOrigin];
  const text = originText(safeOrigin);

  selectedOrigin = safeOrigin;

  document.documentElement.dataset.origin = selectedOrigin;
  localStorage.setItem("hellenesOrigin", selectedOrigin);

  document.querySelectorAll("[data-bind='originName']").forEach(el => {
    el.textContent = text.name;
  });

  document.querySelectorAll("[data-bind='originSymbol']").forEach(el => {
    el.innerHTML = `
      <img
        class="origin-logo-img"
        src="${origin.logo}"
        alt="${text.name} logo"
        loading="lazy"
      >
    `;
  });

  document.querySelectorAll("[data-bind='originMood']").forEach(el => {
    el.textContent = text.mood;
  });

  document.querySelectorAll("[data-bind='rankFlavour']").forEach(el => {
    el.textContent = text.rankFlavour;
  });

  const bonusRoot = document.getElementById("originPreviewBonus");
  if (bonusRoot) {
    bonusRoot.innerHTML = text.bonuses.map(bonus => `<div>${bonus}</div>`).join("");
  }

  const originInput = document.querySelector("input[name='origin']");
  if (originInput) {
    originInput.value = selectedOrigin;
  }

  updateRegistrationCardsSelection();
  updateHomeOriginDetail();

  if (!options.skipLanguageApply) {
    applyLanguage(currentLanguage);
  }
}

function renderAttributes() {
  const root = document.getElementById("attributesList");
  if (!root) return;

  root.innerHTML = ATTRIBUTES.map(attribute => {
    const text = attribute[currentLanguage] || attribute.en;

    return `
      <div class="stat-row">
        <span class="stat-icon ${attribute.danger ? "danger" : ""}">${attribute.icon}</span>
        <div>
          <strong class="stat-name ${attribute.danger ? "danger" : ""}">${text.name}</strong>
          <small class="stat-desc">${text.desc}</small>
        </div>
        <div class="bar"><span style="width:${Math.min(attribute.value, 100)}%"></span></div>
        <strong class="stat-value ${attribute.danger ? "danger" : ""}">${attribute.value}</strong>
      </div>
    `;
  }).join("");
}

function renderOriginCards() {
  const root = document.getElementById("originCards");
  if (!root) return;

  root.innerHTML = Object.entries(ORIGINS).map(([key, origin]) => {
    const isSelected = key === selectedOrigin;
    const text = originText(key);

    return `
      <article
        class="origin-card ${isSelected ? "is-selected" : ""}"
        style="--card-primary:${origin.primary}; --card-glow:${origin.glow};"
        data-origin-card="${key}"
      >
        <div>
          <div class="origin-symbol">
            <img class="origin-logo-img" src="${origin.logo}" alt="${text.name} logo" loading="lazy">
          </div>
          <h3>${text.name}</h3>
          <p><strong>${text.mood}</strong></p>
          <p>${text.description}</p>
          <div class="origin-bonus">
            <strong>${currentLanguage === "el" ? "Προνόμιο Καταγωγής" : "Origin Bonus"}</strong><br>
            ${text.bonuses.join("<br>")}
          </div>
        </div>

        <button type="button">${isSelected ? (currentLanguage === "el" ? "Επιλεγμένη" : "Selected") : (currentLanguage === "el" ? "Προβολή" : "View Panel")}</button>
      </article>
    `;
  }).join("");

  root.querySelectorAll("[data-origin-card]").forEach(card => {
    card.addEventListener("click", () => setOrigin(card.dataset.originCard));
  });
}

function wireRegistrationOriginCards() {
  document.querySelectorAll("[data-registration-origin]").forEach(card => {
    card.addEventListener("click", () => {
      setOrigin(card.dataset.registrationOrigin);
    });
  });
}

async function postJson(url, payload) {
  const response = await fetch(url, {
    method: "POST",
    headers: {
      "Content-Type": "application/json"
    },
    body: JSON.stringify(payload)
  });

  let result = {};
  try {
    result = await response.json();
  } catch {}

  if (!response.ok) {
    throw new Error(result.error || "Request failed.");
  }

  return result;
}

async function submitRegistration(event) {
  event.preventDefault();

  const form = event.currentTarget;
  const message = document.getElementById("registerMessage") || document.getElementById("formMessage");
  const payload = Object.fromEntries(new FormData(form).entries());

  if (!payload.characterName || !payload.characterName.trim()) {
    payload.characterName = "Alexios";
  }

  if (message) {
    message.textContent = t("state.creating");
  }

  try {
    const result = await postJson("/api/register", payload);
    localStorage.setItem("hellenesOrigin", result.player.origin);
    window.location.href = "/character.html";
  } catch (error) {
    if (message) {
      message.textContent = error.message;
    }
  }
}

async function submitLogin(event) {
  event.preventDefault();

  const form = event.currentTarget;
  const message = document.getElementById("loginMessage");
  const payload = Object.fromEntries(new FormData(form).entries());

  if (message) {
    message.textContent = t("state.entering");
  }

  try {
    const result = await postJson("/api/login", payload);
    if (result.player && result.player.origin) {
      localStorage.setItem("hellenesOrigin", result.player.origin);
    }
    window.location.href = "/character.html";
  } catch (error) {
    if (message) {
      message.textContent = error.message;
    }
  }
}

async function loadCharacterFromServer() {
  if (!document.body || !document.body.classList.contains("character-body")) return;

  try {
    const response = await fetch("/api/me/character");

    if (response.status === 401) {
      window.location.href = "/auth.html?mode=login";
      return;
    }

    if (!response.ok) {
      return;
    }

    const result = await response.json();
    const player = result.player;

    if (player.origin) {
      setOrigin(player.origin);
    }

    document.querySelectorAll("[data-bind='characterName']").forEach(el => {
      el.textContent = player.character_name || "Alexios";
    });

    const genosValue = document.querySelector(".identity-list div:nth-child(1) dd");
    if (genosValue && player.genos) {
      genosValue.textContent = player.genos;
    }
  } catch {
    /* Static preview is allowed; server-backed character data simply will not load. */
  }
}

async function logout() {
  try {
    await postJson("/api/logout", {});
  } catch {}
  localStorage.removeItem("hellenesPlayerId");
  window.location.href = "/auth.html?mode=login";
}

function applyAuthModeHighlight() {
  if (!document.body || !document.body.classList.contains("auth-body")) return;

  const params = new URLSearchParams(window.location.search);
  const mode = params.get("mode");

  document.getElementById(mode === "login" ? "loginPanel" : "registerPanel")?.classList.add("is-emphasised");
}

